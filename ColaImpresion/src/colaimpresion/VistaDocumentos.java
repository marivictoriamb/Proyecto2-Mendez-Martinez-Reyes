    public void LlenadoTabla(java.awt.event.ActionEvent evt){
        int fila = tablaUsuarios.getRowCount();

        for (int i = fila-1; i >=0; i--) {
            modelo.removeRow(i);
        }
        Documento[] documentos = usuario.getDocumentos();

        for (int i = 0; i < documentos.length; i++) {
            String nombredoc = documentos[i].getNombreDocumento();
            String tipodoc = documentos[i].getTipoDocumento();
            int tama単odoc = documentos[i].getTama単o();
            String encolado= "";
            if(documentos[i].isEncolado()==false){
                encolado = "No";
            }else{
                encolado = "Si";
            }
            modelo.addRow(new Object[]{nombredoc,tipodoc,tama単odoc,encolado });
        }
    }    
    
